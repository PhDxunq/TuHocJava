public class Bai3_ToanTu {
    public static void main(String[] args){
        int num_1 = 10;
        int num_2 = 5;
        System.out.println("First Number: " + num_1);
        System.out.println("Second Number: " + num_2);
        // Add
        System.out.println("Add: " + (num_1 + num_2));

        // Subtraction
        System.out.println("Subtraction: " + (num_1 - num_2));

        // Multiplication
        System.out.println("Multiplication: " + (num_1 * num_2));

        // Division
        System.out.println("Division: " + (num_1/num_2));

        // Toán tử gán
        /*
        * Gán bằng : x = 1
        * Gán cộng : x += y <=> x = x + y
        * Gán trừ : x -= y <=> x = x - y
        * Gán nhân : x *= y <=> x = x * y
        * Gán chia : x /= y <=> x = x / y
        * Gán chia lấy dư : x %= y <=> x = x % y
        */

        //Toán tử logic
        /*
        * && : and
        * || : or
        * |  : not
        * */

        //Tiền tố prefix, postfix
        /*
        * a++, a-- -> postfix
        * ++a, --a -> prefix
        * Ưu tiên prefix -> các phép toán còn lại -> gán gtri cho biến ở bên trái dấu " = " -> profix (Chú ý quan trọng)
        * */


    }
}
